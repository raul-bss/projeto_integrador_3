    private void abreTela(){
        campoCredito.setText(String.valueOf(pagamento.getValorCredito()));
        campoDebito.setText(String.valueOf(pagamento.getValorDebito()));
        campoDinheiro.setText(String.valueOf(pagamento.getValorDinheiro()));
        campoPix.setText(String.valueOf(pagamento.getValorPix()));
    }
    
    private void finalizaPagamento(){
        pagamento.setTotalPago(totalPago);
        pagamento.setTroco(valorTroco);
        pagamento.setValorCredito(valorCredito);
        pagamento.setValorDebito(valorDebito);
        pagamento.setValorDinheiro(valorDinheiro);
        pagamento.setValorPix(valorPix);
        
        telaVendas.setPagamento(pagamento);
        
        telaVendas.atualizaPagamento();
        
        this.dispose();
    }
    private boolean validaCampo(String valor){
        String regex = "^((\\d{1,3}(\\.\\d{3})*)|\\d+)(,\\d{2})?$";      
        
        if(valor.matches(regex)){
            return true;
        }
        
        return false;
    }
    
    private void calculaTotalPago(){
        totalPago = valorCredito + valorDebito + valorDinheiro + valorPix;
        lblValorPago.setText(String.valueOf(totalPago));
    }
    
    
    
    private void filtroTexto(KeyEvent evt){
        String caracteres = "012345678,.";
        
        if(!caracteres.contains(evt.getKeyChar()+"")){
            evt.consume();
        }
    }
    
