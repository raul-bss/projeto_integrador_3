    private void campoCreditoFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_campoCreditoFocusLost
        String input = campoCredito.getText();
        
        if(input.isBlank() || input == "0"){
            valorCredito = 0;
        }
        
        if(validaCampo(input)){
            Double valor = Double.valueOf(input.replace(",","."));
            valorCredito = valor;
            calculaTotalPago();
        }
    }//GEN-LAST:event_campoCreditoFocusLost

